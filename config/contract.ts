// ___________ Helper: Get proof for address __________

export function getProofForAddress(
    address: string
): `0x${string}`[] | null {
    const normalizedAddress = address.toLowerCase();
    const proof = Object.entries(WHITELIST_PROOFS).find(
        ([key]) => key.toLowerCase() === normalizedAddress
    );
    return proof ? proof[1] : null;
}